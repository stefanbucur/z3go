func decodeGame(game string) (result [][]int) {
	result = make([][]int, 9)
	for r := 0; r < 9; r++ {
		result[r] = make([]int, 9)
		for c := 0; c < 9; c++ {
			ch := game[r*9+c]
			if ch == '.' {
				result[r][c] = 0
			} else {
				result[r][c] = int(ch - '0')
			}
		}
	}
	return
}

